$(function () {
    $("#empire").click(function () {
        $("#empire").hide();
    });
    
    $("#liberty").click(function () {
        $("#liberty").hide();
    });

    $("#times").click(function () {
        $("#times").hide();
    });

    $("#rockf").click(function () {
        $("#rockf").hide();
    });

    $("#showAll").click(function () {
        $("#empire").show();
        $("#liberty").show();
        $("#times").show();
        $("#rockf").show();

    });

});